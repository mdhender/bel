package router

import (
"path"
"strings"
)

// Split returns the head and tail of the path passed in.
// The path will be cleaned of relative components before processing.
// Head will be the first component of the cleaned path.
// The head will never contain a slash.
// Tail will be the remainder of the cleaned path.
// The tail will always be a rooted path without trailing slash.
func Split(p string) (head, tail string) {
	p = path.Clean("/" + p)
	i := strings.Index(p[1:], "/") + 1
	if i <= 0 {
		return p[1:], "/"
	}
	return p[1:i], p[i:]
}
